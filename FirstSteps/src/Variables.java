public class Variables {
    public static void main(String[] args) {

       int number = 1;
       double number2 = 1.234d;
       float number3 = 1.234f;
       String name = "Conrad";
       boolean trueFalse = true;
       char Character = 'A';

        System.out.println("Integer (whole number): " + number + "\n" +
                "Double (Number with decimal places): " + number2 + "\n" +
                "Float (Number with decimal places): " + number3 + "\n" +
                "String (Word or Sentence): " + name + "\n" +
                "Boolean (Statement that indicates true or false): " + trueFalse + "\n" +
                "Char (Single character): " + Character);

    }
}
